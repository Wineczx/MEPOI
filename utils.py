def ndcg_last_timestep(y_true_seq, y_pred_seq, k):
    y_true = [y_true_seq[-1]]  
    y_pred = y_pred_seq[-1]

    dcg_k = 0
    top_k_rec = y_pred.argsort()[-k:][::-1] 
    rel = 0
    for i in range(k):
        if top_k_rec[i] == y_true:
            rel = 1
            break

    dcg_k += rel / np.log2(i + 2)

    idcg_k = 0
    for i in range(min(k, len(y_true))):
        idcg_k += 1 / np.log2(i + 2)

    ndcg_k = dcg_k / idcg_k

    return ndcg_k

def array_round(x, k=4):
    # For a list of float values, keep k decimals of each element
    return list(np.around(np.array(x), k))

def ids2tokens(ids, tokenizer, eos):
    text = tokenizer.decode(ids)
    text = postprocessing(text)  # process punctuations: "good!" -> "good !"
    tokens = []
    for token in text.split():
        if token == eos:
            break
        tokens.append(token)
    return tokens
def postprocessing(string):
    '''
    adopted from https://github.com/yoonkim/CNN_sentence/blob/master/process_data.py
    '''
    string = re.sub('\'s', ' \'s', string)
    string = re.sub('\'m', ' \'m', string)
    string = re.sub('\'ve', ' \'ve', string)
    string = re.sub('n\'t', ' n\'t', string)
    string = re.sub('\'re', ' \'re', string)
    string = re.sub('\'d', ' \'d', string)
    string = re.sub('\'ll', ' \'ll', string)
    string = re.sub('\(', ' ( ', string)
    string = re.sub('\)', ' ) ', string)
    string = re.sub(',+', ' , ', string)
    string = re.sub(':+', ' , ', string)
    string = re.sub(';+', ' . ', string)
    string = re.sub('\.+', ' . ', string)
    string = re.sub('!+', ' ! ', string)
    string = re.sub('\?+', ' ? ', string)
    string = re.sub(' +', ' ', string).strip()
    return string



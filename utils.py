def set_all_seeds(seed):
    """
    设置所有可能影响模型训练可重复性的种子。

    参数:
    seed: int, 要设置的随机数种子。
    """
    random.seed(seed) # Python内置随机库
    os.environ['PYTHONHASHSEED'] = str(seed) # Python哈希随机性
    np.random.seed(seed) # NumPy随机库
    torch.manual_seed(seed) # PyTorch随机库
    torch.cuda.manual_seed(seed) # PyTorch CUDA随机库 (当前设备)
    torch.cuda.manual_seed_all(seed) # PyTorch CUDA随机库 (所有设备)
    transformers_set_seed(seed) # Transformers库随机库
    
    # 确保PyTorch的CUDA行为具有确定性
    torch.backends.cudnn.benchmark = False
    torch.backends.cudnn.deterministic = True
    torch.backends.cudnn.enabled = True
    torch.use_deterministic_algorithms(False)
    
    # 设置CUDA的工作空间配置，有助于确保模型的可重复性
    os.environ['CUBLAS_WORKSPACE_CONFIG'] = ':16:8'

def fit_delimiter(string='', length=80, delimiter="="):
    result_len = length - len(string)
    half_len = math.floor(result_len / 2)
    result = delimiter * half_len + string + delimiter * half_len
    return result


